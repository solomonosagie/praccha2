
public class Trigonometry {

	        double angle =0.0;
	    // Method to calculate sine
	    public  double calculateSin(double angle) {
	        return Math.sin(angle);
	    }
	    // Method to calculate cosine
	    public  double calculateCos(double angle) {
	        return Math.cos(angle);
	    }
	    
	    public  double calculateTan(double angle) {
	        return Math.tan(angle);
	    }
	    // Method to calculate sin inverse
	    public  double calculateSinInverse(double angle) {
	        return Math.asin(angle);
	    }
	    
	    public  double calculateCosInverse(double angle) {
	        return Math.acos(angle);
	    }
	    public  double calculateTanInverse(double angle) {
	        return Math.atan(angle);
	    }
	
	
	
	    
}
